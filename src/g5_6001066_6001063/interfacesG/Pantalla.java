        private void addDragFunctionality() {
        // Listener para detectar cuando se presiona el mouse en titleBarJP
        titleBarJP.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent evt) {
                // Guardar la posici贸n del mouse cuando se presiona
                mouseX = evt.getX();
                mouseY = evt.getY();
            }
        });

        // Listener para mover la ventana mientras se arrastra el mouse en titleBarJP
        titleBarJP.addMouseMotionListener(new MouseMotionAdapter() {
            @Override
            public void mouseDragged(MouseEvent evt) {
                // Obtener la posici贸n actual del mouse y ajustar la posici贸n de la ventana
                int x = evt.getXOnScreen() - mouseX;
                int y = evt.getYOnScreen() - mouseY;
                // Mover la ventana a la nueva ubicaci贸n
                setLocation(x, y);
            }
        });
    }
    
